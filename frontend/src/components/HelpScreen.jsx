export default function HelpScreen() {
  const [openIdx, setOpenIdx] = useState(0)

  return (
    <div className="help-screen">
      <div className="help-hero">
        <HelpCircle size={36} strokeWidth={1.8} color="#1a73e8" />
        <h1>ヘルプ・よくある質問</h1>
        <p>NICOX のご利用にあたって、よく寄せられる質問をまとめました。</p>
      </div>

      <div className="help-list">
        {FAQ.map((item, i) => {
          const open = openIdx === i
          return (
            <div key={i} className={`help-item ${open ? 'open' : ''}`}>
              <button
                className="help-question"
                onClick={() => setOpenIdx(open ? -1 : i)}
                aria-expanded={open}
              >
                {open ? <ChevronDown size={16} /> : <ChevronRight size={16} />}
                <span>{item.q}</span>
              </button>
              {open && <div className="help-answer">{item.a}</div>}
            </div>
          )
        })}
      </div>

      <div className="help-footer">
        <p>
          解決しない場合は、運営管理者までお問い合わせください。
        </p>
      </div>
    </div>
  )
}
